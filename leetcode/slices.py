a = [0, 1, 2, 3, 4, 5, 6]

print(a[0:])  # [0,1,2,3,4,5,6]
print(a[1:])  # [1,2,3,4,5,6]
print(a[0:3])  # [0,1,2]
print(a[1:3])  # [1,2]
print(a[:3])    # [0,1,2]

print(a[0])   # 0
print(a[-1])  # 6
print(a[-2])  # 5

print(a[-1:])  # [6]
print(a[-2:])  # [5,6]
print(a[-3:])  # [4,5,6]
print(a[:-1])  # [0,1,2,3,4,5]
print(a[:-2])  # [0,1,2,3,4]

# print(a[::0]) # error
print(a[::1])  # [0,1,2,3,4,5,6]
print(a[::2])  # [0,2,4,6]
print(a[::-1])  # [6,5,4,3,2,1,0]
print(a[::-2])  # [6,4,2,0]

print(a[1::2])  # [1,3,5]
print(a[-2:-1])  # [5]
print(a[-3:-1])  # [4,5]
